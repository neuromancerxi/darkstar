function onMobDeath(mob, player, isKiller)
	if (math.random(100) < 40) then  
        if (not player:hasKeyItem(SEVERED_GIGAS_COLLAR)) then
            player:addKeyItem(SEVERED_GIGAS_COLLAR);
            player:messageSpecial(SEVERED_GIGAS_COLLAR);
        end
    end
end;
